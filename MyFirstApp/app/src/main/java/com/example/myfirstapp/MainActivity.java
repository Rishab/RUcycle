    public void sendPost(final String base64) {
        Thread thread = new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    URL url = new URL("https://amperess.lib.id/recycle@dev/");
                    HttpURLConnection conn = (HttpURLConnection) url.openConnection();
                    conn.setRequestMethod("POST");
                    conn.setRequestProperty("Content-Type", "application/json;charset=UTF-8");
                    conn.setRequestProperty("Accept","application/json");
                    conn.setDoOutput(true);
                    conn.setDoInput(true);

                    JSONObject jsonParam = new JSONObject();
                    jsonParam.put("img", base64);

                    Log.i("TAGJSON", jsonParam.toString());
                    DataOutputStream os = new DataOutputStream(conn.getOutputStream());
                    //os.writeBytes(URLEncoder.encode(jsonParam.toString(), "UTF-8"));
                    os.writeBytes(jsonParam.toString());

                    os.flush();
                    os.close();

                    Log.i("TAGSTATUS", String.valueOf(conn.getResponseCode()));
                    Log.i("TAGMSG" , conn.getResponseMessage());

                    DataInputStream is = new DataInputStream(conn.getInputStream());

                    String response = inputStreamToString(conn.getInputStream());

                    Log.v("TAGRESPONSE", response);
                    conn.disconnect();
                    if (response.equals("N")) {
                        openRecycleActivity(response);
                    }
                    else if (response.equals("E")) {
                        openRecycleActivity(response);
                    }
                    else {
                        openTrashActivity(response);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        });

        thread.start();
    }
    public String inputStreamToString(InputStream inputStream) throws IOException {
        try(ByteArrayOutputStream result = new ByteArrayOutputStream()) {
            byte[] buffer = new byte[1024];
            int length;
            while ((length = inputStream.read(buffer)) != -1) {
                result.write(buffer, 0, length);
            }

            return result.toString("UTF-8");
        }
    }
